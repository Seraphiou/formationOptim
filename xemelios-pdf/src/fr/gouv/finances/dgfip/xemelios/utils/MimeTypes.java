    public static String getMimeType(String filename) {
        String ret = null;
        String ext = " ";
        int pos = filename!=null ? filename.lastIndexOf('.') : -1;
        if(pos>=0) {
            ext = filename.substring(pos+1).toLowerCase();

        }
        ret = MIME_TYPES.get(ext);
        return ret;
    }
